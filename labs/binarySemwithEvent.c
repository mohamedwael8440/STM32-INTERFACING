void T_button(void* pvData){
	Uart_SendString(USART2,"T_button started...\n");
	while(1){
if(Btn_isPressed(BTN_RIGHT)){
	xSemaphoreGive(bsBtnRightPreesedEvent);
	while(Btn_isPressed(BTN_RIGHT)){
		vTaskDelay(10);
	}

}
if(Btn_isPressed(BTN_LEFT)){
	xSemaphoreGive(bsBtnLeftPreesedEvent);
	while(Btn_isPressed(BTN_LEFT)){
		vTaskDelay(10);
	}
}
if(Btn_isPressed(BTN_UP)){
		xSemaphoreGive(bsBtnUpPreesedEvent);
		while(Btn_isPressed(BTN_UP)){
			vTaskDelay(10);
		}
}
if(Btn_isPressed(BTN_DOWN)){
			xSemaphoreGive(bsBtnDownPreesedEvent);
			while(Btn_isPressed(BTN_DOWN)){
				vTaskDelay(10);
			}
}
    vTaskDelay(50);//os delay 100 tick, one tick = one mil second
	}
}



